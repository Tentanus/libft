#include "libft.h"

/** @brief
 *  Checks if c is alphabetic character on the ASCII table:
 * 
 *  @param c		Character to be checked.
 *  @return int		True if c is alphabetic character, false if not.
 **/

int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
